package D1Array;

public class PrintArray {
    public static void main(String[] args) {
        //create array
        int[] arr={2,3,6,4,5,9};

        int[] arr1={8,9,5,3,6,7,8};

        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]+" ");
        }
        System.out.println("");

        //print second array 02
        for (int i = 0; i < arr1.length; i++) {
            System.out.print(arr1[i]+" ");
        }
        System.out.println("");
    }
}
